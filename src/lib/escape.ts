/**
 * Escape user-controlled strings before any HTML injection path.
 * Prefer React text children; keep this for rare raw HTML needs.
 */
export function escapeHtml(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}
